def add2(x):
    return x + 2


def subtract2(x):
    return x - 2


def multiple2(x):
    return x * 2


def devide2(x):
    return x / 2

def power2(x):
    return x ** 2

def devide2getn(x):
    return x // 2


num = int(input('Input a number:'))
print('add2(num): ',add2(num))
print('subtract2(num): ',subtract2(num))
print('multiple2(num): ',multiple2(num))
print('devide2(num): ',devide2(num))
print('power2(num): ',power2(num))
print('devide2getn(num): ',devide2getn(num))
